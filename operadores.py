# OPERAÇÕES MATEMÁTICAS

"""
    -SOMA: +
    -SUBTRAÇÃO: -
    -MULTIPLICAÇÃO: *
    -DIVISÃO: /
    -DIVISÃO INTEIRA: // (ignora as casas decimais)
    -RESTO DA DIVISÃO: %
    -POTÊNCIA: **
"""

num1 = 10
num2 = 20

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 // num2)
print(20 % 3)
print(2 ** 3)

# OPERADORES BOOLEANOS (TRUE OR FALSE)

"""
    -MAIOR QUE: >
    -MENOR QUE: <
    -MAIOR OU IGUAL A: >=
    -MENOR OU IGUAL A: <=
    -IGUAL: == (diferencia maiúsculo de minusculo, letras, palavras, mas não por quantidade de letras)
    -DIFERENTE: !=
"""

idade1 = 10
idade2 = 15
altura1 = 1.77
altura2 = 1.65
altura3 = altura2

print(idade1 > idade2)
print(idade1 <= idade2)
print('Python' == 'python')
print('letra' != 'palavra')
print(altura1 >= altura2)
print(altura2 < altura3)
